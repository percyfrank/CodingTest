'''
===========================================================================================
입력 조건
-------------------------------------------------------------------------------------------
* 첫째 줄에 N(2 <= N <= 100,000)과 K(2 <= K <= 100,000)가 공백으로 구분되며 각각 자연수로
주어진다. 이때 입력으로 주어지는 N은 항상 K보다 크거나 같다.
===========================================================================================
출력 조건
-------------------------------------------------------------------------------------------
* 첫째 줄에 N이 1이 될 때까지 1번 혹은 2번의 과정을 수행해야 하는 횟수의 최솟값을 출력한다.
===========================================================================================
'''

n,k = map(int,input().split())
result = 0

while True:
    target = (n//k) * k
    result += n - target
    n = target
    if n < k:
        break; 
    
    result += 1
    n //= k
    
result += (n-1)
    
print(result)
    
    
